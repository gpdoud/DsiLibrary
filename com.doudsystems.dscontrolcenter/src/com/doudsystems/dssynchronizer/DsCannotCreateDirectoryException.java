package com.doudsystems.dssynchronizer;

public class DsCannotCreateDirectoryException extends DsException
{

	public DsCannotCreateDirectoryException()
	{
	}

	public DsCannotCreateDirectoryException(String message)
	{
		super(message);
	}

	public DsCannotCreateDirectoryException(Throwable cause)
	{
		super(cause);
	}

	public DsCannotCreateDirectoryException(String message, Throwable cause)
	{
		super(message, cause);
	}

}
